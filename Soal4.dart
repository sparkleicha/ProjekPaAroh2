void main() {
  int a = 5;
  int b = 10;

  //perkalian
  int perkalian = a * b;
  print('$a * $b = $perkalian');

  //penjumlahan
  int penjumlahan = a + b;
  print('$a + $b = $penjumlahan');

  //pengurangan
  int pengurangan = a - b;
  print('$a - $b = $pengurangan');

  //pembagian
  double pembagian = a / b; // Hasil pembagian akan menjadi tipe data double karena b adalah 10 (tipe data double).
  print('$a / $b = $pembagian');
}
